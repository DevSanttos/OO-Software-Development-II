    private void adicionarBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarBotaoActionPerformed
        try {
            if (numeros.size() >= 5) {
                JOptionPane.showMessageDialog(this, "Já foram inseridos 5 números!", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int numero = Integer.parseInt(jTextField1.getText());
            numeros.add(numero);
            jTextField1.setText("");
            textoResposta.setText("Números inseridos: " + numeros);

            if (numeros.size() == 5) {
                calcularBotao.setEnabled(true);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Digite um número inteiro válido!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_adicionarBotaoActionPerformed

