    private void calcularBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularBotaoActionPerformed
         if (numeros.size() < 5) {
            JOptionPane.showMessageDialog(this, "Insira 5 números antes de calcular!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int maior = Collections.max(numeros);
        int menor = Collections.min(numeros);

        textoResposta.setText("Números digitados: " + numeros +
                             "\n Maior número: " + maior +
                             "\n Menor número: " + menor);
    }//GEN-LAST:event_calcularBotaoActionPerformed

